function AddSelectedLink($string)
	{
	return('<span class="background">'.$string.'</span> ');
	}

function AddSelectLink($string,$href)
	{
	return('<a href="'.$href.'" class="SelectLink">'.$string.'</a> ');
	}

function AppendLink($qs, $category, $value, $wantedValue)
{
	if($value == $wantedValue)
		return AddSelectedLink($value);
		
	return AddSelectLink($wantedValue, '?'.RebuildQS(array($category => $wantedValue)));
}


	
function LinkTab($title,$link)
	{
	if (isset($_GET['fview']) && $_GET['fview'] != '' && ChopToScript($link) == 'friends.php')
		{
		$title_class = "HeadLinksSelect";
		$a_class = "AHeadSelect";	
		}	
	else
		{
		if (ChopToScript($link) == ChopToScript($_SERVER['PHP_SELF']) && !(isset($_GET['fview']) && $_GET['fview'] != ''))
			{
			$title_class = "HeadLinksSelect";
			$a_class = "AHeadSelect";	
			}
		else
			{
			$title_class = "HeadLinks";	
			$a_class = "AHead";
			}
		}
	echo('<div class="'.$title_class.'">
<a target="_top" class="'.$a_class.'" href="'.$link.'">'.$title.'</a>
</div>
	');
	}		

function LinkTab2($title,$link,$active = true)
	{
	if (isset($_GET['id']) && $_GET['id'] != '')
		$id = '?id='.$_GET['id'];
	if (ChopToScript($link) == ChopToScript($_SERVER['PHP_SELF']).$id)
		$title_class = "HeadLinks2Select";	
	else
		$title_class = "HeadLinks2";	
	if ($active)
		echo('<div class="'.$title_class.'"><a target="_parent" href="'.$link.'">'.$title.'</a></div>');
	else
		echo('<div class="HeadLinks2"><span class="greyout">'.$title.'</span></div>');
	}	


