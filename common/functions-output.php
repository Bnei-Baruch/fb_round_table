function TagLink($string,$inlink = false)
	{
	$in=array(
        '`((?:https?|ftp)://\S+[[:alnum:]]/?)`si',
        '`((?<!//)(www\.\S+[[:alnum:]]/?))`si'
        );
	if (stristr($string,'www.grumper.org'))
		$target = '';
	else
		$target = 'target="_blank"';
	if ($inlink)
		{
		$out=array(
       	 '</a><a href="$1"  class="blue" '.$target.' >$1</a><a '.$inlink.'>',
       	 '</a><a href="http://$1" class="blue" '.$target.' >$1</a><a '.$inlink.'">'
		 );
		}
	else
		{
		$out=array(
       	 '<a href="$1"  class="blue" '.$target.' rel="nofollow">$1</a> ',
       	 '<a href="http://$1" class="blue" '.$target.' rel="nofollow">$1</a>'
			);
		}
	$string = preg_replace($in, $out, $string);
	return($string);	
	}	

